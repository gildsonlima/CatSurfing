public class Doenca {
    
    private String nome;
    private boolean temCura;
    private boolean contagiosa;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public boolean isTemCura() {
        return temCura;
    }

    public void setTemCura(boolean temCura) {
        this.temCura = temCura;
    }

    public boolean isContagiosa() {
        return contagiosa;
    }

    public void setContagiosa(boolean contagiosa) {
        this.contagiosa = contagiosa;
    }
    
    
}
